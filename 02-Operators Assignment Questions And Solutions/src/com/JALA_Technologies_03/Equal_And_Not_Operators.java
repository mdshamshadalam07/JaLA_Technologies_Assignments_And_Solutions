package com.JALA_Technologies_03;

public class Equal_And_Not_Operators {

	public static void main(String[] args) {
		int x = 5;
		int y = 3;

		// Equal Operator two values.
		System.out.println(x == y); // // returns false because 5 is not equal to 3
		System.out.println();

		int a = 5;
		int b = 3;

		// Not Equal Operator two values.
		System.out.println(a != b); // returns true because 5 is not equal to 3
		System.out.println();
	}
}
